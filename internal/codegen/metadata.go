// ToGoName converts a SQL column name to a Go field name.
// Example: created_at -> CreatedAt, is_enabled -> IsEnabled, source_id -> SourceID
func ToGoName(sqlName string) string {
	parts := strings.Split(sqlName, "_")
	for i, part := range parts {
		if len(part) == 0 {
			continue
		}
		upper := strings.ToUpper(part)
		// Handle common Go acronyms
		switch upper {
		case "ID":
			parts[i] = "ID"
			continue
		case "URL", "URI", "URN", "UUID":
			parts[i] = upper
			continue
		case "IO", "OS", "HTTP", "TCP", "UDP", "SQL":
			parts[i] = upper
			continue
		}
		// Capitalize first letter
		runes := []rune(part)
		runes[0] = unicode.ToUpper(runes[0])
		parts[i] = string(runes)
	}
	return strings.Join(parts, "")
}

// ToGoType converts a Walens type constant to actual Go type name.
func ToGoType(typeConst string) string {
	switch typeConst {
	case TypeUUID:
		return "dbtypes.UUID"
	case TypeBoolInt:
		return "dbtypes.BoolInt"
	case TypeUnixMilliTime:
		return "dbtypes.UnixMilliTime"
	case TypeUnixMilliDuration:
		return "dbtypes.UnixMilliDuration"
	case TypeRawJSON:
		return "dbtypes.RawJSON"
	case TypeString:
		return "string"
	case TypeInt64:
		return "int64"
	case TypeFloat64:
		return "float64"
	case TypeBytes:
		return "[]byte"
	default:
		return "any"
	}
}
