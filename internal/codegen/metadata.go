// ClassifyColumnForGenerator classifies a column for model generation using
// Go-Jet metadata. This combines the column classification with nullable handling.
// It prefers using the provided dbType if available, otherwise falls back to
// name-based classification.
func ClassifyColumnForGenerator(tableName string, columnName string, isNullable bool, dbType string) (goType string, wrappedType string, needsPointer bool) {
	if tableName == "configs" && columnName == "value" {
		goType = TypeRawJSON
		wrappedType = ToGoType(goType)
		needsPointer = isNullable
		return goType, wrappedType, needsPointer
	}

	// If dbType is provided and not empty, use it for classification
	if dbType != "" {
		goType, _ = ClassifyColumn(columnName, dbType)
	} else {
		// Fall back to name-based classification
		goType, _ = ClassifyColumnByName(columnName)
	}
	wrappedType = ToGoType(goType)
	needsPointer = isNullable
	return goType, wrappedType, needsPointer
}

