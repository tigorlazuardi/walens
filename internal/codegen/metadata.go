// ValidateIdentifierName checks if a name follows Walens naming conventions.
// Internal IDs should be "id" or "*_id", external IDs should be "*_identifier".
func ValidateIdentifierName(name string) error {
	if name == "" {
		return fmt.Errorf("identifier name cannot be empty")
	}
	lower := strings.ToLower(name)

	// Check for external ID misuse (should be *_identifier, not *_id)
	if strings.HasSuffix(lower, "_id") && !strings.HasSuffix(lower, "_identifier") {
		// This might be an external ID that should use *_identifier instead
		// Allow if it looks like a standard internal ID pattern
		standardInternalIDs := map[string]bool{
			"id":          true,
			"source_id":   true,
			"device_id":   true,
			"image_id":    true,
			"tag_id":      true,
			"job_id":      true,
			"config_id":   true,
			"schedule_id": true,
		}
		if !standardInternalIDs[lower] {
			return fmt.Errorf("column %q ends with _id but may be an external identifier; use _identifier for external/source-owned IDs", name)
		}
	}

	return nil
}

