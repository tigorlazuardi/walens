// GetColumnDoc returns the documentation for a specific column, or empty if not found.
func GetColumnDoc(tableName, columnName string) ColumnDoc {
	table, ok := WalensSchemaDocs[tableName]
	if !ok {
		return ColumnDoc{}
	}
	col, ok := table.Columns[columnName]
	if !ok {
		return ColumnDoc{}
	}
	return col
}

// GetTableDoc returns the documentation for a specific table, or nil if not found.
func GetTableDoc(tableName string) *TableDoc {
	table, ok := WalensSchemaDocs[tableName]
	if !ok {
		return nil
	}
	return &table
}

// ValidateSchemaDocs checks that documentation exists for all columns in a table.
// Returns an error listing any missing columns.
func ValidateSchemaDocs(tableName string, actualColumns []string) error {
	table, ok := WalensSchemaDocs[tableName]
	if !ok {
		return fmt.Errorf("table %q not found in schema docs", tableName)
	}

	var missing []string
	for _, col := range actualColumns {
		if _, ok := table.Columns[col]; !ok {
			missing = append(missing, col)
		}
	}

	if len(missing) > 0 {
		return fmt.Errorf("table %q is missing documentation for columns: %v", tableName, missing)
	}
	return nil
}

// DocTag builds a doc tag string from a description.
func DocTag(description string) string {
	if description == "" {
		return ""
	}
	// Escape double quotes for Go string literal
	escaped := strings.ReplaceAll(description, "\"", "\\\"")
	return fmt.Sprintf(`doc:"%s"`, escaped)
}

// BuildColumnTags builds the full struct tag string for a column.
func BuildColumnTags(jsonName string, goType string, col ColumnDoc) string {
	var parts []string
	if jsonName != "" {
		parts = append(parts, fmt.Sprintf("json:%q", jsonName))
	}
	if col.Description != "" {
		parts = append(parts, DocTag(col.Description))
	}
	return strings.Join(parts, " ")
}

