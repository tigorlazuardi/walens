// Package codegen provides Walens-specific Go-Jet generation tooling,
// metadata mapping helpers, and post-processing utilities.
//
// This package centralizes the mapping rules that govern how database
// columns are translated into Go types in generated model code:
//   - UUID mapping for id and *_id columns
//   - Unix-millisecond timestamp mapping for named timestamp columns
//   - Duration mapping for *_ms columns
//   - Boolean mapping for is_* columns
//   - RawJSON mapping for json_* columns
//   - Documentation tag generation for doc:"..." struct tags
package codegen

